extension AppI18nExtension on BuildContext {
  String tr(String key) {
    final locale = watch<LocaleController>().locale.languageCode;
    return AppI18n.translate(locale, key);
  }

  /// 带参数替换：trArg('bizRefereeStakeN', {'n': '2'}) → "被推广人2质押"
  String trArg(String key, Map<String, String> args) {
    var text = tr(key);
    args.forEach((k, v) {
      text = text.replaceAll('{$k}', v);
    });
    return text;
  }
